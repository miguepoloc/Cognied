create  view view_respuesta_encuestas as
select
	e.id_encuesta as id_encuesta,
	e.nombre as encuesta,
	u.id_usuario as id_usuario,
	u.nombre as nombre_usuario,
	p.id_pregunta as id_pregunta,
	p.pregunta as pregunta,
	r.id_respuesta as id_respuesta,
	r.respuesta as respuesta,
	r.valor as valor
from
 usuario u
join usuario_encuesta ue
           on
	u.id_usuario = ue.id_usuario
join usuario_respuesta ur
          on
	ur.id_usuario_encuesta = ue.id_usuario_encuesta join pregunta_respuesta  pr
         on  pr.id_pregunta_respuesta =  ur.id_pregunta_respuesta join  pregunta  p
        on  p.id_pregunta =  pr.id_pregunta join   respuesta  r
       on  r.id_respuesta =  pr.id_respuesta join   encuesta  e
      on  ue.id_encuesta =  e.id_encuesta;



